# ASSERT

# assert basically is a function that checks if the condition is true or not.
# if the condition is true, then the program will continue to run.
# if the condition is false, then the program will stop and print the error message.

assert 3 == 3, "3 is not equal to 3"
print("It past the first assert!")
assert 3 == 5, "3 is not equal to 5"
